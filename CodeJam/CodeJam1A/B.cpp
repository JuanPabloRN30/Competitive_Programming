#include <bits/stdc++.h>

using namespace std;

int main()
{
  int t;
  scanf("%d",&t);
  for(int i = 0 ; i < t ; i++)
  {
    int n;
    scanf("%d",&n);
    for(int i = 0 ; i < 2*n ; i++)
    {
      int val;
      for(int j = 0 ; j < n ; j++)
      {
        scanf("%d",&val);
      }
    }
  }


  return 0;
}
